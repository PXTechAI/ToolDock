const languageButtons = document.querySelectorAll("[data-language]");
const preferredLanguage = localStorage.getItem("tooldock-site-language");
const browserLanguage = navigator.language;
const initialLanguage =
  preferredLanguage ||
  (browserLanguage.startsWith("zh")
    ? "zh-CN"
    : browserLanguage.startsWith("ja")
      ? "ja"
      : browserLanguage.startsWith("ko")
        ? "ko"
        : "en");

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;
  document.documentElement.lang = language;
  localStorage.setItem("tooldock-site-language", language);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAlt];
    if (value) {
      element.setAttribute("alt", value);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function detectRepository() {
  const githubPagesMatch = window.location.hostname.match(/^([^.]+)\.github\.io$/);
  const repositoryName = window.location.pathname.split("/").filter(Boolean)[0];

  if (!githubPagesMatch || !repositoryName) {
    return null;
  }

  return `https://github.com/${githubPagesMatch[1]}/${repositoryName}`;
}

const repositoryUrl = detectRepository();

document.querySelectorAll(".repository-link").forEach((link) => {
  if (repositoryUrl) {
    link.href = repositoryUrl;
  } else {
    link.href = "../";
  }
});

document.querySelectorAll(".releases-link").forEach((link) => {
  if (repositoryUrl) {
    link.href = `${repositoryUrl}/releases/latest`;
  } else {
    link.href = "../releases";
  }
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.language));
});

setLanguage(initialLanguage);
