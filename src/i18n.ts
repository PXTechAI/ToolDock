export function translate(language: AppLanguage, key: string, params: Params = {}): string {
  const template = dictionaries[language]?.[key] ?? zhCN[key] ?? key;
  return Object.entries(params).reduce(
    (value, [name, replacement]) => value.replaceAll(`{${name}}`, String(replacement)),
    template,
  );
}

export function createTranslator(language: AppLanguage) {
  return (key: string, params?: Params) => translate(language, key, params);
}

export function localeFor(language: AppLanguage) {
  return {
    "zh-CN": "zh-CN",
    en: "en-US",
    ja: "ja-JP",
    ko: "ko-KR",
  }[language];
}

export function storedLanguage(): AppLanguage {
  try {
    const raw = window.localStorage.getItem("tooldock-settings");
    const language = raw ? JSON.parse(raw).language : null;
    if (language === "zh-CN" || language === "en" || language === "ja" || language === "ko") {
      return language;
    }
  } catch {
    // Fall through to the browser language.
  }
  const browserLanguage = navigator.language.toLowerCase();
  if (browserLanguage.startsWith("ja")) return "ja";
  if (browserLanguage.startsWith("ko")) return "ko";
  if (browserLanguage.startsWith("en")) return "en";
  return "zh-CN";
}
